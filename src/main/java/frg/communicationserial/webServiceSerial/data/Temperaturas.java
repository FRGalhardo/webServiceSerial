package frg.communicationserial.webServiceSerial.data;

public class Temperaturas {
	
	private int umidade;
	
	private int tempCelsius;
	
	private int tempFahrenheit;
	
	private int tempCaixaCelsius;

	public int getUmidade() {
		return umidade;
	}

	public void setUmidade(int umidade) {
		this.umidade = umidade;
	}

	public int getTempCelsius() {
		return tempCelsius;
	}

	public void setTempCelsius(int tempCelsius) {
		this.tempCelsius = tempCelsius;
	}

	public int getTempFahrenheit() {
		return tempFahrenheit;
	}

	public void setTempFahrenheit(int tempFahrenheit) {
		this.tempFahrenheit = tempFahrenheit;
	}

	public int getTempCaixaCelsius() {
		return tempCaixaCelsius;
	}

	public void setTempCaixaCelsius(int tempCaixaCelsius) {
		this.tempCaixaCelsius = tempCaixaCelsius;
	}
}
